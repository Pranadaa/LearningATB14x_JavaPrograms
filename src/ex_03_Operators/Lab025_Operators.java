package ex_03_Operators;

public class Lab025_Operators {
    public static void main(String[] args) {

        int a=10;
        int b=20;
        float c= 3.05f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(b/a);
        System.out.println(a*b);

        System.out.println("a+b");
        System.out.println(b%a);
        System.out.println(a/c);
        System.out.println(a-c);
    }
}
