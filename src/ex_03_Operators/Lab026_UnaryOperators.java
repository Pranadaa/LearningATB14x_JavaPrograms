package ex_03_Operators;

public class Lab026_UnaryOperators {
    public static void main(String[] args) {

        // Unary Operator
        int a = +10;
        int a1 = -110;
        int result = a+a1;

        int b = -1;
        b = b+1;
        System.out.println(b);
        System.out.println(result);
    }
}
